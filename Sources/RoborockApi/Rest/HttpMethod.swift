import Foundation

enum HttpMethod: String {
    case get = "GET"
    case post = "POST"
    case put = "PUT"
    case delete = "DELETE"
    case patch = "PATCH"
    case head = "HEAD"
    case trace = "TRACE"
    case connect = "CONNECT"
    case options = "OPTIONS"
}
